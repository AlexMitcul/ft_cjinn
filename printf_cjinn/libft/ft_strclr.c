#include "libft.h"

void	ft_strclr(char *s)
{
	char			*str;
	unsigned int	c;

	str = (char *)s;
	c = 0;
	if (str)
	{
		while (str[c])
		{
			str[c] = '\0';
			c++;
		}
	}
}
